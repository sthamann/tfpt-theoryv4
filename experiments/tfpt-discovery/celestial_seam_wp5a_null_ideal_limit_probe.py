# root-system machinery in DOUBLED integer coordinates (all roots norm_d = 8;
# true inner product = ip_d / 4)
# ---------------------------------------------------------------------------
class RootSystem:
    def __init__(self, name, simple, roots):
        self.name = name
        self.simple = [tuple(a) for a in simple]
        self.roots = [tuple(r) for r in roots]
        self.dim = len(simple[0])
        self.n = len(simple)
        assert self.dim == self.n
        # rows of the "columns = simple roots" matrix, for alpha-coordinates
        self.Mrows = [[self.simple[j][i] for j in range(self.n)]
                      for i in range(self.dim)]
        self.cartan = [[ip(a, b) // 4 for b in self.simple]
                       for a in self.simple]
        pos = [r for r in self.roots if self._nonneg_coords(r)]
        assert 2 * len(pos) == len(self.roots)
        self.pos = pos
        s = [0] * self.dim
        for r in pos:
            s = [x + y for x, y in zip(s, r)]
        assert all(x % 2 == 0 for x in s)
        self.rho = tuple(x // 2 for x in s)          # doubled rho
        # fundamental weights (doubled): ip(w_i, alpha_j) = 4 delta_ij
        self.fund = []
        for i in range(self.n):
            b = [4 if j == i else 0 for j in range(self.n)]
            rows = [[self.simple[j][k] for k in range(self.dim)]
                    for j in range(self.n)]
            w = solve_fraction(rows, b)
            assert all(x.denominator == 1 for x in w)
            self.fund.append(tuple(int(x) for x in w))

    def _nonneg_coords(self, v):
        cs = solve_fraction(self.Mrows, list(v))
        return all(c >= 0 for c in cs)

    def alpha_coords(self, v):
        return solve_fraction(self.Mrows, list(v))

    def in_Qplus(self, v):
        cs = self.alpha_coords(v)
        return all(c.denominator == 1 and c >= 0 for c in cs)

    def height(self, v):
        cs = self.alpha_coords(v)
        h = sum(cs)
        assert h.denominator == 1
        return int(h)

    def fold(self, v):
        """dominant Weyl-chamber representative (simple reflections)."""
        v = list(v)
        changed = True
        while changed:
            changed = False
            for a in self.simple:
                c = 0
                for x, y in zip(v, a):
                    c += x * y
                if c < 0:
                    k = c // 4          # ip_d divisible by 4 on the lattice
                    assert c % 4 == 0
                    for t in range(self.dim):
                        v[t] -= k * a[t]
                    changed = True
        return tuple(v)

    def is_dominant(self, v):
        return all(ip(v, a) >= 0 for a in self.simple)

    def weyl_dim(self, lam):
        lr = vadd(lam, self.rho)
        out = F(1)
        for a in self.pos:
            out *= F(ip(lr, a), ip(self.rho, a))
        assert out.denominator == 1
        return int(out)

    def dominant_candidates(self, lam):
        """all dominant weights mu with lam - mu in Q+ (exact enumeration)."""
        nl = ip(lam, lam)
        G = [[ip(wi, wj) for wj in self.fund] for wi in self.fund]
        bounds = [isqrt(nl // G[i][i]) for i in range(self.n)]
        out = []
        for ns in product(*(range(b + 1) for b in bounds)):
            mu = tuple(sum(ns[i] * self.fund[i][k] for i in range(self.n))
                       for k in range(self.dim))
            if ip(mu, mu) <= nl and self.in_Qplus(vsub(lam, mu)):
                out.append(mu)
        return out

    def freudenthal(self, lam):
        """dominant weight multiplicities of V(lam) (exact Freudenthal).

        The k-sum runs over the FULL cone lam - (mu + k a) in Q+ (no early
        break on zero multiplicity -- alpha-strings of non-weights need not
        be contiguous); multiplicities of non-dominant nu are read off via
        Weyl folding.  Alpha-coordinates are precomputed so the cone test
        is a monotone integer comparison."""
        lam = tuple(lam)
        if all(x == 0 for x in lam):
            return {lam: 1}
        cands = sorted(self.dominant_candidates(lam),
                       key=lambda m: self.height(vsub(lam, m)))
        acoords = [(a, [int(c) for c in self.alpha_coords(a)])
                   for a in self.pos]
        mult = {}
        lr = vadd(lam, self.rho)
        nlr = ip(lr, lr)
        for mu in cands:
            if mu == lam:
                mult[lam] = 1
                continue
            cmu = [int(c) for c in self.alpha_coords(vsub(lam, mu))]
            num = 0
            for a, ca in acoords:
                k = 1
                while all(cm - k * c >= 0 for cm, c in zip(cmu, ca)):
                    nu = vadd(mu, smul(k, a))
                    m = mult.get(self.fold(nu), 0)
                    if m:
                        num += m * ip(nu, a)
                    k += 1
            mr = vadd(mu, self.rho)
            den = nlr - ip(mr, mr)
            assert den > 0 and (2 * num) % den == 0
            m_mu = (2 * num) // den
            if m_mu > 0:
                mult[mu] = m_mu
        return mult

    def orbit_size(self, v):
        v = tuple(v)
        seen = {v}
        frontier = [v]
        while frontier:
            nf = []
            for x in frontier:
                for a in self.simple:
                    c = ip(x, a)
                    if c == 0:
                        continue
                    y = tuple(xi - (c // 4) * ai for xi, ai in zip(x, a))
                    if y not in seen:
                        seen.add(y)
                        nf.append(y)
            frontier = nf
        return len(seen)

    def dim_by_orbits(self, mult):
        return sum(m * self.orbit_size(mu) for mu, m in mult.items())


def sym2_dominant_tally(rs, wts):
    """dominant-weight multiplicities of Sym^2 of the weight list.

    Weight multiplicities are Weyl-invariant, so the multiplicity of a
    DOMINANT weight mu equals the number of unordered pairs summing to mu
    itself (no folding -- folding would conflate whole Weyl orbits).
    Returns (tally over dominant weights, total number of pairs)."""
    tal = {}
    n = len(wts)
    npairs = 0
    for i in range(n):
        wi = wts[i]
        for j in range(i, n):
            npairs += 1
            w = tuple(a + b for a, b in zip(wi, wts[j]))
            if rs.is_dominant(w):
                tal[w] = tal.get(w, 0) + 1
    return tal, npairs


def peel(rs, tally):
    """decompose a dominant-multiplicity tally into irreducibles (generic)."""
    residual = dict(tally)
    comps = []
    for _ in range(12):
        support = [mu for mu, v in residual.items() if v != 0]
        if not support:
            break
        assert all(residual[mu] > 0 for mu in support)
        lam = max(support, key=lambda mu: (rs.height(mu), mu))
        mult = residual[lam]
        fm = rs.freudenthal(lam)
        for mu, m in fm.items():
            residual[mu] = residual.get(mu, 0) - mult * m
        comps.append((lam, mult, rs.weyl_dim(lam), fm))
    assert all(v == 0 for v in residual.values())
    return comps


