def chi_w_gens(w, dims, umax):
    """generator dict energy -> count for the chiral jet family at weight w.
    chiral generators: loop energy m >= 1, radial number r >= 0 (dim
    g_{(-m) mod 4}), plus the invariant-ring tower m = 0, r >= 1 (dim g_0);
    energy E_w = m + w r in quarter units."""
    gens = {}
    for m in range(1, umax + 1):
        dimg = dims[(-m) % 4]
        r = 0
        while m + w * r <= umax:
            e = m + w * r
            gens[e] = gens.get(e, 0) + dimg
            r += 1
    r = 1
    while w * r <= umax:
        gens[w * r] = gens.get(w * r, 0) + dims[0]
        r += 1
    return gens


