# ---- Shimura lift
# Shimura 1973 / standard modern form (Cipra): for
#   g = sum_{n>=1} b(n) q^n  of weight k+1/2  (here k=2),
#   square-free t >= 1, character chi_t mod 4tN with
#   chi_t(d) = chi(d) * ( (-1)^k t / d ),
#   A_t(n) = sum_{d|n} chi_t(d) * d^{k-1} * b(t n^2 / d^2).
# Then Sh_t(g) = sum A_t(n) q^n has weight 2k = 4.
#
# Source convention used HERE (explicit):
#   k = 2, d^{k-1} = d^1 = d,
#   chi_t(d) = psi(d) * kronecker( ((-1)**k) * t , d )
#            = psi(d) * kronecker( t , d )   since (-1)^k = 1 for k=2,
#   with trial nebentypus psi in {1, chi4, chi_{-4}}.
#   t in {1, 2, 5, 10, 13} square-free (small search).
#
# We lift the q^1 and higher coefficients; b(0) is ignored (cusp/Eisenstein
# constant term does not enter A_t(n) for n>=1).

K_SH = 2  # weight = K_SH + 1/2
assert 2 * K_SH == 4


def shimura_A(bq, n, t, psi):
    """A_t(n) = sum_{d|n} psi(d)*kronecker(t,d)*d^{k-1}*b(t n^2/d^2)."""
    total = 0
    for d in sp.divisors(n):
        d = int(d)
        idx = t * (n // d) * (n // d)
        if idx >= len(bq):
            return None  # out of table
        chi = psi(d) * kronecker(t, d)
        total += chi * (d ** (K_SH - 1)) * bq[idx]
    return int(total)


def shimura_lift_series(bq, t, psi, nmax):
    out = [0] * (nmax + 1)
    for n in range(1, nmax + 1):
        val = shimura_A(bq, n, t, psi)
        if val is None:
            return None
        out[n] = val
    return out


