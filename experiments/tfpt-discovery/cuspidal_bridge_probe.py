    """Classical local for sum a_{p^m}^2 X^m, weight 4, chi=1.

    Unreduced: D_p = (1 - p^3 X^2) / [(1-a^2 X)(1-b^2 X)(1-p^3 X)^2].
    Cancel (1 - p^3 X^2)/(1 - p^3 X)^2 = (1 + p^3 X)/(1 - p^3 X)
    (u = p^3 X) => reduced den degree 3:
      D_p = (1 + p^3 X) / [(1 - p^3 X) (1 - (a_p^2 - 2 p^3) X + p^6 X^2)].
    """
