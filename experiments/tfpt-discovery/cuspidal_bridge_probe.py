def monomial_t(a0, a2, b0, b2, c0, c2, order_t):
    """Build theta2^{a0} theta2(q^2)^{a2} ... in t-units (int64)."""
    s = np.zeros(order_t + 1, dtype=np.int64)
    s[0] = 1
    for _ in range(a0):
        s = conv_i(s, _th(2, 1, order_t), order_t)
    for _ in range(a2):
        s = conv_i(s, _th(2, 2, order_t), order_t)
    for _ in range(b0):
        s = conv_i(s, _th(3, 1, order_t), order_t)
    for _ in range(b2):
        s = conv_i(s, _th(3, 2, order_t), order_t)
    for _ in range(c0):
        s = conv_i(s, _th(4, 1, order_t), order_t)
    for _ in range(c2):
        s = conv_i(s, _th(4, 2, order_t), order_t)
    return s


def to_q_series(s_t, qmax):
    """Extract integer q-coeffs: q^n <-> t^{4n}.  Null if fractional."""
    for r in range(1, 4):
        if np.any(s_t[r::4] != 0):
            return None
    out = [0] * (qmax + 1)
    lim = min(qmax, (len(s_t) - 1) // 4)
    for n in range(lim + 1):
        out[n] = int(s_t[4 * n])
    return out


