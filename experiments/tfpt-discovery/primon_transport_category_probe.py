def eta_pow(d: int, e: int, order: int) -> Series:
    s = zeros(order)
    s[0] = 1
    for n in range(1, order // d + 1):
        f = zeros(d * n)
        # length d*n+1
        f = [0] * (d * n + 1)
        f[0], f[d * n] = 1, -1
        for _ in range(e):
            s = pmul(s, f, order)
    return s


def shift(a: Sequence[int], k: int, order: int) -> Series:
    return ([0] * k + list(a))[: order + 1]


