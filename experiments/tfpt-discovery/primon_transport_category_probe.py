# ---------------------------------------------------------------- series ops
def zeros(order: int) -> Series:
    return [0] * (order + 1)


def pmul(a: Sequence[int], b: Sequence[int], order: int) -> Series:
    """Exact integer convolution truncated to order (Python ints)."""
    res = zeros(order)
    for i, ai in enumerate(a):
        if not ai:
            continue
        if i > order:
            break
        bj_max = order - i
        for j in range(min(len(b) - 1, bj_max) + 1):
            bj = b[j]
            if bj:
                res[i + j] += ai * bj
    return res


def ppow(a: Sequence[int], e: int, order: int) -> Series:
    res = zeros(order)
    res[0] = 1
    for _ in range(e):
        res = pmul(res, a, order)
    return res


def padd(a: Sequence[int], b: Sequence[int]) -> Series:
    n = max(len(a), len(b))
    out = [0] * n
    for i in range(n):
        out[i] = (a[i] if i < len(a) else 0) + (b[i] if i < len(b) else 0)
    return out


def psub(a: Sequence[int], b: Sequence[int]) -> Series:
    n = max(len(a), len(b))
    out = [0] * n
    for i in range(n):
        out[i] = (a[i] if i < len(a) else 0) - (b[i] if i < len(b) else 0)
    return out


def phalf(a: Sequence[int]) -> Series:
    assert all(x % 2 == 0 for x in a)
    return [x // 2 for x in a]


def pscale(c: int, a: Sequence[int], order: int) -> Series:
    return [c * a[n] for n in range(order + 1)]


def peq(a: Sequence[int], b: Sequence[int], order: int) -> bool:
    return all(int(a[n]) == int(b[n]) for n in range(order + 1))


def theta3_q(step: int, order: int) -> Series:
    s = zeros(order)
    s[0] = 1
    n = 1
    while step * n * n <= order:
        s[step * n * n] += 2
        n += 1
    return s


def theta4_q(step: int, order: int) -> Series:
    s = zeros(order)
    s[0] = 1
    n = 1
    while step * n * n <= order:
        s[step * n * n] += 2 * ((-1) ** n)
        n += 1
    return s


