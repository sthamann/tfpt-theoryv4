def hecke_Tn(a: Sequence[int], n: int, k: int = 4,
             out_order: int | None = None) -> Series:
    """Classical weight-k Hecke operator (trivial nebentypus):
    (T_n f)(m) = sum_{d | gcd(m,n)} d^{k-1} a(m n / d^2).
    """
    if n <= 0:
        raise ValueError("n must be positive")
    N = len(a) - 1
    M = out_order if out_order is not None else N // n
    out = zeros(M)
    km1 = k - 1
    # divisors of n once
    divs_n = [d for d in range(1, n + 1) if n % d == 0]
    for m in range(M + 1):
        total = 0
        g = math.gcd(m, n)
        for d in divs_n:
            if g % d != 0:
                continue
            idx = m * n // (d * d)
            if 0 <= idx <= N:
                total += (d ** km1) * a[idx]
        out[m] = total
    return out


def hecke_Tp(a: Sequence[int], p: int, k: int = 4,
             out_order: int | None = None) -> Series:
    """(T_p f)(n) = f(p n) + p^{k-1} f(n/p)  [latter 0 if p!|n]."""
    N = len(a) - 1
    M = out_order if out_order is not None else N // p
    out = zeros(M)
    pk = p ** (k - 1)
    for n in range(M + 1):
        pn = p * n
        term = a[pn] if pn <= N else 0
        if n % p == 0:
            term = term + pk * a[n // p]
        out[n] = term
    return out


# Cross-check T_n vs iterative prime composition on a test form
def factor_Tn_from_primes(a: Sequence[int], n: int, k: int = 4,
                          out_order: int = CHK) -> Series:
    """Build T_n via prime-power ladder + coprime product (classical)."""
    if n == 1:
        return list(a[: out_order + 1])
    res = list(a)
    # factor n
    rem = n
    p = 2
    factors: List[Tuple[int, int]] = []
    while p * p <= rem:
        e = 0
        while rem % p == 0:
            rem //= p
            e += 1
        if e:
            factors.append((p, e))
        p += 1 if p == 2 else 2
    if rem > 1:
        factors.append((rem, 1))
    # need headroom for intermediate applications
    src_needed = out_order * n + 2
    if len(res) - 1 < src_needed:
        # res already has full a; ok if a long enough
        pass
    cur_order = len(a) - 1
    # apply T_{p^e} factors (commutative)
    # T_{p^e} via recurrence from T_p
    def Tp_power(series: Series, p_: int, e_: int, ord_: int) -> Series:
        if e_ == 0:
            return list(series[: ord_ + 1])
        if e_ == 1:
            return hecke_Tp(series, p_, k=k, out_order=ord_)
        # build T_{p^j} for j=1..e by ladder:
        # T_p T_{p^j} = T_{p^{j+1}} + p^{k-1} T_{p^{j-1}}
        # => T_{p^{j+1}} = T_p T_{p^j} - p^{k-1} T_{p^{j-1}}
        # We need enough source; work at high order then truncate.
        hi = len(series) - 1
        Tpow: List[Series] = [list(series[: hi + 1])]  # T_{p^0}=Id
        Tpow.append(hecke_Tp(series, p_, k=k, out_order=hi // p_))
        for j in range(1, e_):
            # T_{p^{j+1}} = T_p (T_{p^j} f) - p^{k-1} T_{p^{j-1}} f
            # apply T_p to the series (T_{p^j} f)
            left = hecke_Tp(Tpow[j], p_, k=k,
                            out_order=min(ord_, len(Tpow[j]) - 1))
            # align lengths
            prev = Tpow[j - 1]
            mlen = min(len(left) - 1, len(prev) - 1, ord_)
            nxt = [left[i] - (p_ ** (k - 1)) * prev[i]
                   for i in range(mlen + 1)]
            Tpow.append(nxt)
        return list(Tpow[e_][: ord_ + 1])

    out = list(a)
    # For coprime product: start from Id applied to a, fold T_{p^e}
    # Working on the series: apply operators left to right.
    # Need generous intermediate order.
    ord_work = out_order
    acc = list(a)
    for p_, e_ in factors:
        acc = Tp_power(acc, p_, e_, ord_work)
    return acc[: out_order + 1]


